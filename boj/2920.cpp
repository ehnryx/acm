void solve_main([[maybe_unused]] int testnum, [[maybe_unused]] auto& cin) {
  vector<int> a(8);
  for(int& i : a) {
    cin >> i;
  }
  vector<int> d;
  adjacent_difference(begin(a), end(a), back_inserter(d));
  if(unique(begin(d)+1, end(d)) == begin(d)+2) {
    cout << (d[1] == 1 ? "ascending" : "descending") << nl;
  } else {
    cout << "mixed" << nl;
  }
}

